#!/usr/bin/env python3
"""
Движок генерации постов для Telegram-бота.

Пайплайн (веб-поиск + критик, разнесён по моделям ради экономии):
  1. Researcher  (Sonnet + web_search) — проверяет факты, выносит ВЕРДИКТ.
  2. ГЕЙТ ФАКТОВ — если факты не подтверждены, пост НЕ пишется (без явного force).
  3. Writer      (Haiku)               — пишет черновики ТОЛЬКО из проверенных фактов.
  4. Critic      (Sonnet)              — сверяет с фактами и бренд-войсом, чистит финал.

Экономия: системный промпт кэшируется, черновик пишет дешёвый Haiku,
дорогой Sonnet только на факт-чек и финальную вычитку.
"""
import json
import re

import anthropic

RESEARCH_MODEL = "claude-sonnet-4-6"
DRAFT_MODEL = "claude-haiku-4-5"
CRITIC_MODEL = "claude-sonnet-4-6"

LABELS = {"telegram": "Telegram", "vk": "ВК", "zen": "Сайт+Дзен"}

# Стабильная часть промпта (бренд-войс) — кэшируется.
SYSTEM_BRAND = """Ты — главный редактор и SMM-стратег канала ImpactLaunch про маркетплейсы,
e-commerce и онлайн-торговлю. Пишешь на уровне сильных деловых медиа: ясно, плотно, с характером.

АУДИТОРИЯ: селлеры, предприниматели, менеджеры маркетплейсов. Ценят конкретику, цифры и
практическую пользу. Ненавидят воду, «инфоцыганщину» и пустой позитив.

ЧТО ДЕЛАЕТ ТЕКСТ СИЛЬНЫМ (применяй всегда):
1. ХУК решает всё. Первая строка цепляет: цифра, конфликт, неожиданность или прямая ставка для
   читателя. Никаких разогревов «Сегодня поговорим о…», «Не секрет, что…».
2. Одна мысль на пост. Не размазывай, режь лишнее.
3. Конкретика вместо абстракций: цифры, сроки, имена, последствия. «Комиссия выросла до 43%»,
   а не «комиссии растут».
4. Польза для читателя: что это значит ДЛЯ НЕГО и что с этим делать.
5. Живой язык: короткие предложения, активный залог, разговорные связки. Пиши как эксперт другу.
6. Финал с пользой или вовлечением, а не «оставайтесь с нами».

ЗАПРЕЩЁННЫЙ AI-МУСОР (ни в коем случае):
- штампы: «в эпоху цифровизации», «в современном мире», «важно отметить», «не секрет, что»,
  «динамично развивается», «играет важную роль», «таким образом»;
- канцелярит и пассив, обилие восклицательных знаков;
- эмодзи в каждой строке и буллеты ради буллетов;
- общие фразы без фактов, вода, повтор одной мысли разными словами.
Эмодзи — точечно, 0–4 на пост, только осмысленные.

ФОРМАТЫ ПЛОЩАДОК:
- telegram: ~400–800 знаков. Хук в первой строке. Абзацы по 1–2 строки, между ними пустая строка.
  Разметка ТОЛЬКО HTML: <b>жирный</b>, <i>курсив</i> (markdown ** и ## запрещены). 1 короткий
  вовлекающий вопрос в конце.
- vk: ~600–1200 знаков. ПЕРВЫЙ абзац (≈150 знаков) — самостоятельный хук, он виден до «показать
  полностью» и должен заставить раскрыть пост. Дальше суть и польза. Без HTML-тегов.
- zen: статья для сайта (её заберёт Дзен). Дзен ранжирует по ДОЧИТЫВАНИЯМ — держи внимание до конца.
  Это ОБЪЕКТ с полями:
  title — заголовок 50–90 знаков: интрига + конкретика, без жёлтого кликбейта и без CAPS;
  summary — лид-анонс 1–2 предложения: обещает пользу, цепляет (без HTML);
  body — HTML, 2500–5000 знаков. Разрешены теги: <p>, <h2>, <h3>, <ul>, <ol>, <li>,
  <blockquote>, <b>, <i>, <a>. title внутрь body НЕ дублируй.
  Структура: сильный лид-абзац → 2–4 смысловых блока с <h2> → абзацы <p> по 2–4 предложения →
  вывод с практической пользой. Каждый подзаголовок продаёт чтение следующего блока.

ДОСТОВЕРНОСТЬ (незыблемо):
- Используй ТОЛЬКО факты, цифры, даты и события из присланного брифа.
- Запрещено добавлять факты, которых нет в брифе, даже если «звучит правдоподобно».
- Помеченное в брифе как «осторожно/не подтверждено» НЕ подавай как факт: оговори
  («по неподтверждённым данным») или опусти.
- Никаких выдуманных обещаний, скидок, процентов и сроков.
Каждую площадку адаптируй под её формат — это РАЗНЫЕ тексты, а не один скопированный."""

SYSTEM_RESEARCH = """Ты — строгий фактчекер и ресёрчер делового новостного канала. Тебе дают
идею/новость. Задача — проверить факты и собрать плотный бриф, на котором редактор напишет пост.

Как работать с источниками:
- Ищи через web_search (особенно цифры, даты, события, названия, проценты, сроки).
- НЕ ограничивайся сниппетами: открой через web_fetch и прочитай ПОЛНОСТЬЮ 2-3 самых
  релевантных источника, прежде чем выносить вердикт.
- Если в идее есть ссылка (URL) — обязательно открой её через web_fetch и прочитай целиком.
- Сверяй ключевые факты минимум по двум независимым источникам. Приоритет: первоисточник
  (пресс-релиз, офиц. сайт, заявление компании) > деловые СМИ > агрегаторы/блоги.
- Отмечай свежесть: когда произошло, не устарела ли новость.
- Различай факт и мнение/прогноз — прогнозы помечай как оценку, не как факт.

Собери КОНКРЕТИКУ, полезную для поста: точные цифры, даты, имена, суть «что изменилось»,
последствия для селлеров/бизнеса, 1-2 цитаты или ярких детали.

Формат ответа СТРОГО такой (первая строка обязательна и ровно одним токеном):

VERDICT: CONFIRMED | PARTIAL | UNCONFIRMED
ФАКТЫ:
- <проверенные факты с конкретикой и источником-сайтом>
ОСТОРОЖНО:
- <что НЕ подтвердилось / требует оговорки> (или «нет»)

Критерии вердикта:
- CONFIRMED — ключевые утверждения подтверждены несколькими независимыми источниками.
- PARTIAL — суть верна, но детали/цифры неточны или подтверждены частично.
- UNCONFIRMED — нет подтверждений, либо похоже на слух/фейк, либо источники противоречат.

Будь честен и осторожен: если сомневаешься между PARTIAL и UNCONFIRMED — ставь UNCONFIRMED."""


def _client(cfg):
    key = cfg.get("anthropic_api_key", "").strip()
    if not key:
        raise RuntimeError(
            "Нет anthropic_api_key в config.json. "
            "Возьми ключ на console.anthropic.com и вставь в config."
        )
    return anthropic.Anthropic(api_key=key)


def _text(resp):
    return "".join(b.text for b in resp.content if b.type == "text").strip()


def _parse_verdict(brief):
    """Достаёт вердикт из брифа. Fail-closed: при неоднозначности — UNCONFIRMED."""
    m = re.search(r"VERDICT:\s*(CONFIRMED|PARTIAL|UNCONFIRMED)", brief, re.I)
    if m:
        return m.group(1).lower()
    low = brief.lower()
    if "не подтверж" in low or "unconfirmed" in low:
        return "unconfirmed"
    if "частичн" in low or "partial" in low:
        return "partial"
    if "подтверж" in low or "confirmed" in low:
        return "confirmed"
    return "unconfirmed"  # не смогли распарсить — считаем непроверенным


def research(client, idea):
    """Sonnet + web_search + web_fetch (читает страницы целиком). -> {'verdict','brief'}."""
    tools = [
        {"type": "web_search_20260209", "name": "web_search"},
        {"type": "web_fetch_20260209", "name": "web_fetch"},
    ]
    messages = [{"role": "user", "content": f"Идея/новость для поста:\n\n{idea}"}]
    resp = None
    for _ in range(10):  # серверные тулы могут вернуть pause_turn — продолжаем
        resp = client.messages.create(
            model=RESEARCH_MODEL,
            max_tokens=8000,
            system=[{"type": "text", "text": SYSTEM_RESEARCH,
                     "cache_control": {"type": "ephemeral"}}],
            tools=tools,
            thinking={"type": "adaptive"},
            output_config={"effort": "medium"},  # чуть глубже ради корректности фактов
            messages=messages,
        )
        if resp.stop_reason == "pause_turn":
            messages.append({"role": "assistant", "content": resp.content})
            continue
        break
    brief = _text(resp)
    return {"verdict": _parse_verdict(brief), "brief": brief}


def _net_prop(n):
    """Схема свойства под площадку. zen — статья-объект для сайта, остальные — строка."""
    if n == "zen":
        return {
            "type": "object",
            "properties": {
                "title": {"type": "string"},
                "summary": {"type": "string"},
                "body": {"type": "string",
                         "description": "HTML: разрешены p,h2,h3,ul,ol,li,blockquote,b,i,a"},
            },
            "required": ["title", "summary", "body"],
            "additionalProperties": False,
        }
    return {"type": "string"}


def _drafts_schema(networks, with_note=False, with_image=False):
    props = {n: _net_prop(n) for n in networks}
    required = list(networks)
    if with_note:
        props["note"] = {"type": "string",
                         "description": "Короткая пометка о достоверности для автора (1 строка)"}
        required.append("note")
    if with_image:
        props["image_prompt"] = {
            "type": "string",
            "description": "Детальный промпт на русском для генерации картинки к посту в Nano Banana Pro"}
        required.append("image_prompt")
    return {"type": "object", "properties": props,
            "required": required, "additionalProperties": False}


def write_drafts(client, idea, brief, networks, verdict):
    """Haiku: черновики ТОЛЬКО из проверенных фактов брифа."""
    nets = ", ".join(LABELS[n] for n in networks)
    hedge = ""
    if verdict != "confirmed":
        hedge = ("\nВНИМАНИЕ: факты подтверждены НЕ полностью. Пиши осторожно, "
                 "не подавай неподтверждённое как факт; спорные детали — с оговоркой "
                 "(«по неподтверждённым данным») или опусти.")
    prompt = (
        f"Идея поста:\n{idea}\n\n"
        f"Проверенный бриф (ЕДИНСТВЕННЫЙ источник фактов):\n{brief}\n"
        f"{hedge}\n\n"
        f"Напиши пост под площадки: {nets}. Это РАЗНЫЕ тексты под формат каждой площадки, "
        "не копия одного. У каждого — сильный хук в первой строке, конкретика из брифа, "
        "польза для читателя-селлера, без AI-штампов и воды.\n"
        f"Верни JSON с ключами: {', '.join(networks)}."
    )
    resp = client.messages.create(
        model=DRAFT_MODEL,
        max_tokens=8000,
        system=[{"type": "text", "text": SYSTEM_BRAND,
                 "cache_control": {"type": "ephemeral"}}],
        output_config={"format": {"type": "json_schema",
                                  "schema": _drafts_schema(networks)}},
        messages=[{"role": "user", "content": prompt}],
    )
    return json.loads(_text(resp))


def critique(client, brief, drafts, networks):
    """Sonnet: сверяет черновики с фактами и бренд-войсом, чистит финал."""
    prompt = (
        f"Проверенный бриф (единственный источник фактов):\n{brief}\n\n"
        f"Черновики постов (JSON):\n{json.dumps(drafts, ensure_ascii=False)}\n\n"
        "Ты — главред. Не просто проверь, а ДОВЕДИ каждый пост до публикации по чек-листу:\n\n"
        "ДОСТОВЕРНОСТЬ:\n"
        "1) Любой факт/цифра/дата ДОЛЖНЫ быть в брифе. Чего нет — удали.\n"
        "2) Неподтверждённое из раздела «ОСТОРОЖНО» — не подавать как факт (оговорка или убрать).\n\n"
        "КАЧЕСТВО (перепиши, если слабо):\n"
        "3) ХУК: первая строка реально цепляет (цифра/конфликт/ставка)? Если нет — перепиши.\n"
        "4) Нет AI-штампов, воды, канцелярита, повторов одной мысли. Режь лишнее.\n"
        "5) Конкретика и польза для селлера на месте.\n"
        "6) Формат площадки: telegram — HTML-теги (не markdown), хук+вопрос, короткие абзацы; "
        "vk — первый абзац самостоятельный хук, без HTML; zen — заголовок 50–90 знаков, "
        "лид, 2–4 <h2>, удержание до конца, тело 2500–5000 знаков.\n"
        "7) Тексты под площадки РАЗНЫЕ и адаптированы, а не один скопированный.\n\n"
        "Верни:\n"
        "- финальные доведённые тексты под те же ключи;\n"
        "- note: краткая честная пометка о достоверности для автора (1 строка);\n"
        "- image_prompt: промпт на русском для картинки к посту в Nano Banana Pro. ЖЁСТКИЕ правила:\n"
        "  • Это ЖИВАЯ СЦЕНА — фотореалистичный кадр или качественная иллюстрация, "
        "НЕ инфографика, НЕ график, НЕ диаграмма со стрелками и процентами (генераторы их портят).\n"
        "  • БЕЗ текста, цифр, подписей и надписей на картинке (модель искажает кириллицу и числа). "
        "Никаких процентов и слов на изображении.\n"
        "  • БЕЗ логотипов и узнаваемых брендов.\n"
        "  • Передай суть поста ОБРАЗОМ или метафорой через реальную сцену: люди, предметы, "
        "обстановка, действие, эмоция.\n"
        "  • Опиши как бриф фотографу/художнику и укажи: главный объект; обстановку/фон; "
        "действие или эмоцию; ракурс и план (крупный/средний/общий); свет; стиль и оптику "
        "(например «editorial-фотография, 35mm, естественный свет, малая глубина резкости» "
        "или «современная 3D-иллюстрация, мягкие тени, чистая композиция»); цветовую палитру; "
        "настроение; формат 16:9.\n"
        "  • Кадр конкретный и небанальный, БЕЗ AI-слопа: никаких абстрактных синих гексагонов, "
        "голограмм, плат, мозгов-нейросетей, светящихся цифровых сеток.\n"
        "  Одним связным абзацем."
    )
    resp = client.messages.create(
        model=CRITIC_MODEL,
        max_tokens=16000,
        system=[{"type": "text", "text": SYSTEM_BRAND,
                 "cache_control": {"type": "ephemeral"}}],
        thinking={"type": "adaptive"},
        output_config={"format": {"type": "json_schema",
                                  "schema": _drafts_schema(networks, with_note=True, with_image=True)},
                       "effort": "medium"},  # главред-доводка: качество, но без обрыва вывода
        messages=[{"role": "user", "content": prompt}],
    )
    txt = _text(resp)
    if not txt:
        raise RuntimeError(f"критик не вернул текст (stop_reason={resp.stop_reason})")
    data = json.loads(txt)
    note = data.pop("note", "")
    image_prompt = data.pop("image_prompt", "")
    return data, note, image_prompt


def generate(cfg, idea, networks, force=False):
    """Полный пайплайн с гейтом фактов.

    networks — список из {'telegram','vk','zen'}. force=True пишет пост даже при
    неподтверждённых фактах (с явной пометкой).

    Возвращает либо:
      {'blocked': True, 'verdict': 'unconfirmed', 'brief': str}   — факты не подтверждены,
      либо
      {'blocked': False, 'drafts': {...}, 'note': str, 'verdict': str, 'brief': str}.
    """
    networks = [n for n in ("telegram", "vk", "zen") if n in networks]
    if not networks:
        raise ValueError("Не выбрана ни одна площадка")
    client = _client(cfg)
    r = research(client, idea)

    # ГЕЙТ: непроверенное не публикуем без явного подтверждения
    if r["verdict"] == "unconfirmed" and not force:
        return {"blocked": True, "verdict": r["verdict"], "brief": r["brief"]}

    drafts = write_drafts(client, idea, r["brief"], networks, r["verdict"])
    final, note, image_prompt = critique(client, r["brief"], drafts, networks)
    return {"blocked": False, "drafts": final, "note": note,
            "image_prompt": image_prompt, "verdict": r["verdict"], "brief": r["brief"]}